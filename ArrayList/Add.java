import java.util.ArrayList;

public class Add {
    public static void main(String[] args) {
        ArrayList<Integer> n = new ArrayList<>();
        n.add(10);
        n.add(20);
        n.add(30);
        n.add(40);
        n.add(50);
        
        // Printing the ArrayList
        for (int num: n) {
            System.out.println(num);
        }
    }
}


